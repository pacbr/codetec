package clases;


public class Pixel {
	private int x;
	private int y;
	private Integer color;
	public void setX(int x) {
		this.x = x;
	}
	public int getX() {
		return x;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getY() {
		return y;
	}
	
	public void setColor(Integer color) {
		this.color = color;
	}
	public Integer getColor() {
		return color;
	}
}
